    los cuales se regirá el acceso y uso del programa de lealtad “HINCHA
DORADO”<strong> </strong>de la aplicación de<strong>AZUL &amp; BLANCO MILLONARIOS F.C S.A</strong>, en adelante<strong>LA APLICACIÓN </strong>, la cual es propiedad de<strong> AZUL &amp; BLANCO MILLONARIOS F.C S.A</strong> Y    <strong>2WAYSPORTS</strong>, en adelante <strong>LOS PROPIETARIOS.</strong>
