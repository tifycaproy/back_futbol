<p>
    <strong>LOS PROPIETARIOS</strong>
    han creado un programa de lealtad “HINCHA DORADO” con el fin de reconocer
    la fidelidad de los usuarios de LA APLIACIÓN; este documento contiene los
términos y condiciones generales que rigen el    <strong>PROGRAMA HINCHA DORADO</strong> y delimita y aclara las condiciones
