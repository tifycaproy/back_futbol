    mismo.
</p>
<p>
    <strong>El PROGRAMA HINCHA DORADO</strong>
    está disponible para descarga gratuita en iOS y Android en todos los
    mercados existentes a nivel mundial.
</p>
<p>
    El usuario se obliga a leer los términos y condiciones aquí establecidos,
    antes de efectuar el registro del <strong>PROGRAMA HINCHA DORADO</strong>,
    en consecuencia, al efectuar el pago si así lo decide, se entenderá que
    conoce, acepta y se somete a lo establecido en los presentes Términos y
    Condiciones.
</p>
<p>
    La inscripción al <strong>PROGRAMA HINCHA DORADO</strong> es onerosa. Solo
    pueden inscribirse personas naturales mayores de trece (13) años
    identificadas con cédula de ciudadanía colombiana, cédula de extranjería
emitida en Colombia o tarjeta de identidad. La inscripción al    <strong>PROGRAMA HINCHA DORADO</strong> automáticamente convierte al
    usuario en miembro y beneficiario del programa, y de esta forma acepta
    íntegramente sus políticas y se obliga a cumplir con los procedimientos,
    términos y condiciones establecidos en el presente reglamento, sus
    documentos modificatorios, aclaratorios y complementarios, y cumplir las
    decisiones <strong>de LOS PROPIETARIOS</strong>, las cuales tendrán
carácter definitivo en todos los asuntos relacionados con el    <strong>PROGRAMA HINCHA DORADO</strong> que estén o no aquí reglamentados.
</p>
<p>
Los menores de edad que quieran hacer parte del    <strong>PROGRAMA HINCHA DORADO </strong>Podrán hacerlo siempre que su(s)
    padre(s) o acudiente(s) asuman las obligaciones que de éste programa se
    derivan.
</p>
<p>
    Se entiende que al suscribirse al <strong>PROGRAMA HINCHA DORADO</strong>,
    se autoriza, conoce, y acepta todas las condiciones y limitaciones
    establecidas en el presente documento.
