<h3>CONCURSO “CONCURSO REFERIDOS APP OFICIAL” DE LA APLICACIÓN OFICIAL DE MILLONARIOS F.C</h3>

<p>La APLICACIÓN OFICIAL DE MILLONARIOS F.C en adelante LA APLICACIÓN AZUL & BLANCO S.A. Y 2WAYSPORTS en adelante LA ORGANIZACIÓN invita a los usuarios inscritos en LA APLICACIÓN interesados en hacer parte del concurso "CONCURSO REFERIDOS APP OFICIAL" para que por medio de LA APLICACIÓN participen por diferentes premios, dando cumplimiento a los siguientes términos y condiciones:

1. El diecisiete (17) de junio del 2018, a las 6:00pm, LA APLICACIÓN premiará al usuario del concurso "CONCURSO REFERIDOS APP OFICIAL" por medio de LA APLICACIÓN.

2. La dinámica del concurso será la siguiente:

a. Existe una única forma de participar: los usuarios mayores de trece (13) años que se encuentren registrados “HINCHA EMBAJADOR OFICIAL” (Usuario que cuente con LA APLICACIÓN descargada y se encuentre registrado en ella)  en LA APLICACIÓN podrán participar en el concurso “CONCURSO REFERIDOS APP OFICIAL” siempre y cuando;

1.	verifiquen su cuenta a través del correo electrónico o mensaje de texto (información suministrada en la planilla de registro o a través del landing page compartido por diferentes redes sociales) y 
2.	acepten los términos y condiciones del concurso.


b. El concurso “CONCURSO REFERIDOS APP OFICIAL” consistirá en que el HINCHA EMBAJADOR OFICIAL que pueda lograr que la mayor cantidad de usuarios nuevos descarguen e ingresen a LA APLICACIÓN y se conviertan en HINCHA EMBAJADOR OFICIAL (esto se logra registrándose a través de la aplicación o por landing page que se les enviara por redes sociales a los nuevos usuarios),y que se encuentren “activos” (es decir que haya descargado  e ingresado LA APLICACIÓN. Para que pueda mantener el estatus de activo deberá entrar a LA APLICACIÓN por lo menos una vez a la semana desde el momento en que ingresó por primera vez a esta hasta la finalización del concurso) será el ganador de diferentes premios. 

•	 Las reglas del concurso serán las siguientes:

Para participar el HINCHA EMBAJADOR OFICIAL, deberá ingresar a la sección de “perfil” y dar clic en el botón “CONCURSO REFERIDOS APP OFICIAL”. Al ingresar a esta sección deberá aceptar términos y condiciones de la misma, al aceptarlos podrá compartir LA APLICACIÓN por medio de diferentes redes sociales para sumar la mayor cantidad de “referidos” (Usuarios nuevos que ha descargado LA APLICACIÓN gracias a la invitación del HINCHA EMBAJADOR OFICIAL) para ser el ganador del concurso. La persona a la que se pretende compartir LA APLICACIÓN para que sume como un referido deberá primero llenar sus datos personales en un landing page o registrarse por medio de Facebook o Google. Al completar dicho registro, podrá descargar la app en las tiendas ya sea App Store o Play Store. Luego  de que tener LA APLICACIÓN descargada deberá ingresar a ella. Cuando el nuevo usuario logré realizar todos estos pasos automáticamente le aparecerá en la lista de referidos al HINCHA EMBAJADOR OFICIAL que lo invitó.

El usuario nuevo deberá entrar a LA APLICACIÓN con el mismo usuario y contraseña o por la misma red social por donde se registro en el link que se le compartió. Al ingresar en la parte superior de la sección de “CONCURSO REFERIDOS APP OFICIAL” le aparecerá el nombre de la persona que la refirió (persona que te invitó a descargar y a usar LA APLICACIÓN). Si esta persona desea participar deberá igualmente acceder al concurso.


En la sección de “CONCURSO REFERIDOS APP OFICIAL” de  LA APLICACIÓN se encontrará una lista con los referidos que el HINCHA EMBAJADOR OFICIAL invitado; si el referido aparece en gris significa que, por un lado, efectivamente  se registró en el landing page pero 1. o no descargo LA APLICACIÓN o 2. si la descargo pero nunca ingresó a esta. Si el referido aparece en color azul significa que se encuentra  activo.


La persona que logre obtener la mayor cantidad de referidos activos, será el ganador de diferentes premios.

Si el ganador del premio es un menor de edad, este solo podrá reclamar el premio acompañado de su padre o madre, validado con registro de nacimiento. 

En caso de empate entre dos o más participantes, el ganador será el usuario que haya registrado de primero en LA APLICACIÓN.

d. Hay diferentes premios. 

e. En cuanto hayan premios se describirán en el momento de apertura del concurso

f. Se deja constancia de que el concurso es de carácter GRATUITO, por lo que en ningún momento se les solicitará a los participantes la entrega de sumas de dinero, números o claves de cuentas bancarias o cualquier otra forma de pago. El premio no puede ser canjeado por dinero, transferido o aplazado. 

g. De serle solicitado al usuario la consignación de algún valor o la entrega de información de carácter financiero, deberá abstenerse de concederla o entregarla, bajo el entendido de que esta no ha sido solicitada por LA ORGANIZACIÓN del concurso y de que puede tratarse de alguna de las modalidades de fraude o piratería informática efectuada por un tercero ajeno a LA ORGANIZACIÓN del concurso.

h. Al participar en el concurso, el usuario declara y acepta que exculpa y excluye de responsabilidad a LA ORGANIZACIÓN, en caso de llegar a divulgar, compartir o entregar información de este tipo o de pagar alguna suma.

i. El ganador declara que participó y ganó sin cometer fraude informático o de otra índole. Si LA ORGANIZACIÓN detecta que se presentó algún tipo fraude el ganador será inmediatamente descalificado y se entregará el premio al siguiente mejor puntaje y así sucesivamente si son varios los ganadores detectados cometiendo fraude.

3. Una vez el ganador del concurso sea notificado por cualquiera de los medios especificados, tendrán plazo hasta de 2 días para manifestar la aceptación del premio y 5 días para presentar los requisitos necesarios, si lo anterior no ocurre antes de la fecha indicada, se entregará el premio al siguiente mejor puntaje y así sucesivamente si este no acepta el premio.

4. Podrán participar únicamente y de forma gratuita personas naturales (excluyendo expresamente a personas jurídicas), mayores de 13 años de edad, que se encuentren inscritos en LA APLICACIÓN y en el concurso “CONCURSO REFERIDOS APP OFICIAL”.

Los seguidores que pretendan participar del concurso deberán cumplir con las siguientes condiciones:

a) Ser mayores de 13 años de edad
b) No ser empleados, personas vinculadas bajo contrato de prestación de servicios, contratistas o administradores de LA ORGANIZACIÓN o parientes de los mismos hasta el primer grado de consanguinidad y afinidad.

En caso de verificarse que un ganador se encontraba dentro de alguno de los vínculos prohibidos al momento de concursar, se anulará su participación y el premio le podrá ser otorgado a otro participante que cumpla efectivamente con todos los requisitos.

5. El premio deberá ser aceptado por medio del correo electrónico suministrado por el usuario al momento de su registro en LA APLICACIÓN junto con su documento de identidad (cédula de ciudadanía o tarjeta de identidad que corresponda con el nombre y número de la persona que se inscribió), salvo que haya otorgado autorización formal escrito autenticada en notaria a un tercero, para que lo represente. Menores de edad deben tener aprobación y consentimiento escrito por parte de su padre, madre o adulto responsable.

6. La entrega de los premios se realizará bajo previo acuerdo de LA ORGANIZACIÓN y el participante vía correo electrónico.

7. Los premios tienen un carácter intransferible, por lo que el ganador no podrá solicitar que sean cedidos, otorgados o entregados a otra persona, tampoco podrán ser sustituidos por dinero en efectivo ni por cualquier otro bien o servicio. 
8. El premio no podrá ser acumulable con otros beneficios o concursos que
realice LA ORGANIZACIÓN

9. LA ORGANIZACIÓN no será responsable por cualquier daño y/o perjuicio, directo o indirecto, que el participante pudiera llegar a sufrir como consecuencia del acceso, utilización, aplicación o procesamiento de información que se surta al interior del concurso “CONCURSO REFERIDOS APP OFICIAL” de LA APLICACIÓN, incluyendo la responsabilidad relacionada con daños causados por virus que infecten los sistemas del participante

10. LA ORGANIZACIÓN no será responsable por aquellas transmisiones electrónicas que estén incompletas, sean confusas, tengan errores, ni por cualquier otro desperfecto técnico que las mismas pudieren tener con motivo o en ocasión de la subida de la información en LA APLICACIÓN

11. Con el fin de dar cumplimiento a lo estipulado en la Ley 1581 de 2012 y normas reglamentarías, el responsable del tratamiento de los datos personales es AZUL & BLANCO S.A., se comunica que los datos personales que los concursantes suministraron voluntariamente, podrán ser utilizados con la finalidad de: (i) Para el desarrollo del concurso; ii) Informar sobre los temas inherentes al concurso; (iii) Usar la fotografía para efectos del concurso o de actividades en LA APLICACIÓN; (iv) Las finalidades asociadas con el funcionamiento y los contenidos en LA APLICACIÓN; iv) Compartirlos con 2WAYSPORTS para efectos comerciales.

Para todo lo anterior el usuario por medio de estos términos y condiciones otorga autorización libre, previa, expresa e informada del tratamiento de sus datos personales. Sus consultas y reclamos, así como cualquier trámite referente al tratamiento de los datos personales debe solicitarlos a través del correo electrónico appmillonarios@2waysports.com

12. LA ORGANIZACIÓN desplegará su mejor esfuerzo y las mejores prácticas en seguridad de la información, pero no podrá garantizar y no será responsable, por la difusión de los datos personales de los participantes por personas ajenas a LA ORGANIZACIÓN, ni por los daños y perjuicios generados como consecuencia de ella

13. LA ORGANIZACIÓN se reserva el derecho de cambiar o establecer fechas y otras restricciones o condiciones al concurso, tales cambios se publicarán oportunamente en LA APLICACIÓN

14. LA ORGANIZACIÓN se reserva el derecho de poner término o modificar el concurso, o descalificar a uno o varios participantes, en caso de existir fraudes o sospechas de fraudes, dificultades técnicas o cualquier otro factor fuera de control de LA ORGANIZACIÓN, que pudieran comprometer la integridad del concurso o que atenten contra el espíritu promocional, competitivo y recreacional del concurso, como por ejemplo el maltrato, matoneo, leguaje inapropiado, soez, grosero, entre concursantes o contra LA ORGANIZACIÓN.
El participante acepta y acuerda que cumplirá con todas las reglas del concurso
y conviene en liberar a LA ORGANIZACIÓN de cualquier reclamación si este
decide ejercer los derechos antes mencionados.

15. La responsabilidad de LA ORGANIZACIÓN finaliza con la puesta a disposición del premio a favor de los ganadores y bajo ningún concepto responderá o reintegrará al ganador cualquier costo y/o gasto en que éste haya incurrido en razón de su participación en el concurso, ni por cualquier otra causa

16. LA ORGANIZACIÓN no será responsable por daños o perjuicios que pudiere sufrir el ganador, sus acompañantes o los terceros con motivo y/u ocasión de la participación en el concurso y/o del uso de los premios declinando todo tipo de responsabilidad contractual y/o extracontractual frente a los ganadores y/o acompañantes y/o sus sucesores, lo que es aceptado incondicionalmente por cada participante con el simple hecho de participar en el concurso, así mismo, LA ORGANIZACIÓN no será responsable por hechos de fuerza mayor o caso fortuito, así como hechos ajenos a la voluntad y obligaciones de LA ORGANIZACIÓN

17. Por el solo hecho de participar en el concurso, el participante acepta y transfiere a LA ORGANIZACIÓN la facultad de disponer de la (s) fotografía (s) aportadas la cual será visible en el ranking generado por el resultado obtenido en su participación en el concurso. Dicha fotografía podrá ser vista por los demás usuarios registrados en LA APLICACIÓN, si estos desearan ver el perfil del usuario dentro del ranking. En caso tal que el usuario no desee que los otros usuarios tengan acceso a su foto de perfil, este no está en la obligación de colocar una.

18. Por el solo hecho de concursar, todos los participantes declaran conocer y
aceptar estos términos y condiciones del concurso. Las decisiones de LA ORGANIZACIÓN sobre todos y cada uno de los aspectos del concurso y estos términos serán definitivos e inapelables.

19. En caso que el potencial ganador no se presentará a retirar el premio en la dirección, días y horarios especificados por LA APLICACIÓN perderá el derecho al mismo, sin opción a reclamo alguno.

20. Será a cargo de los ganadores la obtención de toda la documentación necesaria para utilizar el premio.

21. LA ORGANIZACIÓN no será responsable por los daños, o actuaciones por fuera de ley que el ganador y los acompañantes hagan mientras estén en la ejecución del premio.

22. 19. Los participantes mediante el ingreso al presente concurso “CONCURSO REFERIDOS APP OFICIAL”, salvo prohibición legal, otorgan a LA ORGANIZACIÓN un permiso exclusivo de uso de sus nombres, fotografías, voces, retratos, videos y testimonio en relación con el presente juego en los medios y formas que e LA ORGANIZACIÓN considere conveniente. Por este medio los participantes renuncian a todo reclamo de regalías, derechos o remuneración por dicho uso.  

20. Esta versión de “CONCURSO REFERIDOS APP OFICIAL” estará activa desde el día veintiocho (28) de febrero de 2018 a las 10:00 a.m. hasta el día quince  (15)  de junio de 2018 a las 23:59 pm.  
